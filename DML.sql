CREATE OR REPLACE VIEW film_actors
AS
	SELECT DISTINCT CONCAT_WS(' ', p.firstname, p.lastname) as person_name, f.id
	FROM films_persons fp
	JOIN films f ON fp.film_id = f.id
	JOIN persons p ON fp.person_id = p.id
	WHERE fp.actor = 1 AND f.id = 1;


CREATE OR REPLACE VIEW film_producer
AS
	SELECT DISTINCT CONCAT_WS(' ', p.firstname, p.lastname) as person_name, f.id
	FROM films_persons fp
	JOIN films f ON fp.film_id = f.id
	JOIN persons p ON fp.person_id = p.id
	WHERE fp.producer = 1 AND f.id = 1;


CREATE OR REPLACE VIEW film_director
AS
	SELECT DISTINCT CONCAT_WS(' ', p.firstname, p.lastname) as person_name, f.id
	FROM films_persons fp
	JOIN films f ON fp.film_id = f.id
	JOIN persons p ON fp.person_id = p.id
	WHERE fp.director_film = 1 AND f.id = 1;



