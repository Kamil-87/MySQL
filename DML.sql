CREATE OR REPLACE VIEW film_info_genres
AS
 	SELECT f.id, f.title, group_concat(DISTINCT g.name ORDER BY g.name ASC SEPARATOR ', ' ) as film_genres
 	FROM film_genres fg
 	JOIN genres g ON g.id = fg.genre_id
 	JOIN films f ON f.id = fg.film_id
 	GROUP BY f.rating, f.title;
 
CREATE OR REPLACE VIEW film_info_category
AS
 	SELECT f.id, f.title, c.name
 	FROM film_category fc
 	JOIN category c ON c.id = fc.category_id
 	JOIN films f ON f.id = fc.film_id;
 


 CREATE OR REPLACE VIEW film_info
 AS 
 	SELECT f.id, f.title, f.description, f.release_year, l.name as `language`, f.length, f.rating, fic.name AS category,  fig.film_genres 
 	FROM 
 		films f
	JOIN `language` l ON l.id = f.original_language_id
 	JOIN film_info_category fic ON f.id = fic.id
	JOIN film_info_genres fig ON f.id = fig.id
	GROUP BY f.rating, f.title;
	
